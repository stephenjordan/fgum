    # WHT round-trip: iWHT(WHT(x)) == x to machine epsilon
    # ══════════════════════════════════════════════════════════════════════════
    @testset "WHT round-trip" begin
        for n in [2, 4, 8, 16, 64, 256]
            x = randn(ComplexF64, n)
            y = QaoaVerifier.iwht(QaoaVerifier.wht(x))
            @test y ≈ x atol=1e-12
        end
    end

    @testset "WHT known values" begin
        # WHT of [1, 0, 0, 0] = [1, 1, 1, 1]
        @test QaoaVerifier.wht([1.0+0im, 0, 0, 0]) ≈ [1, 1, 1, 1]
        # WHT of [1, 1, 1, 1] = [4, 0, 0, 0]
        @test QaoaVerifier.wht([1.0+0im, 1, 1, 1]) ≈ [4, 0, 0, 0]
    end

    @testset "xor_convolution_power" begin
        x = ComplexF64[1.0, 0.5, 0.3, 0.2]
        # Power 1 should return the input
        @test QaoaVerifier.xor_convolution_power(x, 1) ≈ x atol=1e-12
        # Power 2 = xor_convolution(x, x) via WHT
        x2 = QaoaVerifier.iwht(QaoaVerifier.wht(x) .^ 2)
        @test QaoaVerifier.xor_convolution_power(x, 2) ≈ x2 atol=1e-12
    end

